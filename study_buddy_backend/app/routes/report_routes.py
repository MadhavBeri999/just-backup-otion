def email_report(parent_email: str, child_name: str, report_text: str):
    """Send the generated report to the parent's email."""
    try:
        msg = EmailMessage()
        msg["Subject"] = f"📊 Weekly Study Report for {child_name} — Padhle Bhai"
        msg["From"] = f"Padhle Bhai!! <{EMAIL_USER}>"
        msg["To"] = parent_email
        msg.set_content(report_text)

        with smtplib.SMTP_SSL("smtp.gmail.com", 465) as server:
            server.login(EMAIL_USER, EMAIL_PASS)
            server.send_message(msg)
        print(f"✅ [report] Study report emailed to {parent_email}")
    except Exception as e:
        print(f"❌ [report] Email failed: {str(e)}")


def get_weekly_data(sessions, ai_prompts):
    """Calculate daily breakdown for the current week."""
    now = datetime.utcnow()
    monday_start = (now - timedelta(days=now.weekday())).replace(
        hour=0, minute=0, second=0, microsecond=0
    )

    daily_data = {
        i: {"day": d, "studyMinutes": 0, "alerts": 0, "queries": 0}
        for i, d in enumerate(["Mon", "Tue", "Wed", "Thu", "Fri", "Sat", "Sun"])
    }

    for session in sessions:
        if session.start_time and session.start_time >= monday_start:
            day_idx = session.start_time.weekday()
            daily_data[day_idx]["alerts"] += session.alert_count
            if session.status == "completed" and session.task:
                daily_data[day_idx]["studyMinutes"] += session.task.duration_minutes
            elif session.status == "terminated" and session.end_time:
                diff = session.end_time - session.start_time
                daily_data[day_idx]["studyMinutes"] += max(
                    1, math.ceil(diff.total_seconds() / 60.0)
                )

    for prompt in ai_prompts:
        if prompt.created_at and prompt.created_at >= monday_start:
            day_idx = prompt.created_at.weekday()
            daily_data[day_idx]["queries"] += 1

    return [daily_data[i] for i in range(7)]


@router.get("/download/{child_id}")
def download_report(
    child_id: int,
    db: Session = Depends(get_db),
    current_parent: Parent = Depends(get_current_parent),
):
    child = (
        db.query(Child)
        .filter(Child.id == child_id, Child.parent_id == current_parent.id)
        .first()
    )
    if not child:
        raise HTTPException(status_code=404, detail="Child not found")

    sessions = db.query(StudySession).filter(StudySession.child_id == child_id).all()
    completed_tasks = (
        db.query(Task).filter(Task.child_id == child_id, Task.is_completed == 1).count()
    )
    ai_prompts = (
        db.query(AIPrompt)
        .filter(AIPrompt.child_id == child_id)
        .order_by(AIPrompt.created_at.desc())
        .all()
    )

    weekly_data = get_weekly_data(sessions, ai_prompts)
    report_text = generate_report_text(child, sessions, completed_tasks, ai_prompts, weekly_data)

    # Also email the report to the parent
    parent = db.query(Parent).filter(Parent.id == current_parent.id).first()
    if parent and parent.email:
        email_report(parent.email, child.name, report_text)

    return PlainTextResponse(
        content=report_text,
        media_type="text/plain",
        headers={
            "Content-Disposition": f'attachment; filename="PadhleBhai_Report_{child.name}_{datetime.utcnow().strftime("%Y%m%d")}.txt"'
        },
    )

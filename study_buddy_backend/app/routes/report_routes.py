    session_lines = ""
    recent = sorted(sessions, key=lambda x: x.start_time or datetime.min, reverse=True)[:10]
    for i, s in enumerate(recent, 1):
        start = s.start_time.strftime("%d %b %Y, %I:%M %p") if s.start_time else "N/A"
        status_emoji = "✅" if s.status == "completed" else "❌"
        task_title = s.task.title if s.task else "Unknown"
        session_lines += f"   {i}. {status_emoji} {task_title} — {start} (Alerts: {s.alert_count})\n"


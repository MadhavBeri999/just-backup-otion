    now_ist = (datetime.utcnow() + timedelta(hours=5, minutes=30)).strftime(
        "%I:%M %p IST on %d %b %Y"
    )

    total_study_minutes = 0
    total_alerts = 0
    completed_sessions = 0
    terminated_sessions = 0

    for s in sessions:
        total_alerts += s.alert_count
        if s.status == "completed":
            completed_sessions += 1
            if s.task:
                total_study_minutes += s.task.duration_minutes
        elif s.status == "terminated":
            terminated_sessions += 1
            if s.start_time and s.end_time:
                diff = s.end_time - s.start_time
                total_study_minutes += max(1, math.ceil(diff.total_seconds() / 60.0))

    hours = total_study_minutes // 60
    mins = total_study_minutes % 60

